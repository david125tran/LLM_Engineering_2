# --------- Helper: pick best available quantization/backend ---------
def build_base_model(model_id: str):
    has_cuda = torch.cuda.is_available()
    # try 4-bit
    if has_cuda:
        try:
            import bitsandbytes as bnb  # noqa: F401
            qconf = BitsAndBytesConfig(load_in_4bit=True)
            print("Loading base model in 4-bit quantizationâ€¦")
            return AutoModelForCausalLM.from_pretrained(
                model_id,
                quantization_config=qconf,
                device_map="auto",
                trust_remote_code=True,
            )
        except Exception as e:
            print(f"âš ï¸ 4-bit load failed: {e}")
            # try 8-bit
            try:
                print("Falling back to 8-bit quantizationâ€¦")
                qconf = BitsAndBytesConfig(load_in_8bit=True)
                return AutoModelForCausalLM.from_pretrained(
                    model_id,
                    quantization_config=qconf,
                    device_map="auto",
                    trust_remote_code=True,
                )
            except Exception as e2:
                print(f"âš ï¸ 8-bit load failed: {e2}")

    # final fallback: full precision (bf16 if supported, else fp16/cpu)
    dtype = torch.bfloat16 if torch.cuda.is_bf16_supported() else (torch.float16 if has_cuda else torch.float32)
    device_map = "auto" if has_cuda else None
    print(f"Falling back to full precision dtype={dtype} (device_map={device_map})â€¦")
    return AutoModelForCausalLM.from_pretrained(
        model_id,
        torch_dtype=dtype,
        device_map=device_map,
        trust_remote_code=True,
    )

